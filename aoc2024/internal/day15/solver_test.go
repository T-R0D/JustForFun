func TestPartOne(t *testing.T) {
	testCases := []struct {
		name     string
		input    string
		expected string
	}{
		{
			name:     "PartOne finds the sum of the box Goods Positioning Sysytem coordinates for a small example",
			input:    smallExampleWarehouseAndRobotMovements,
			expected: "2028",
		},
		{
			name:     "PartOne finds the sum of the box Goods Positioning Sysytem coordinates",
			input:    exampleWarehouseAndRobotMovements,
			expected: "10092",
		},
	}

	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			t.Parallel()

			solver := Solver{}

			result, err := solver.SolvePartOne(tc.input)
			if err != nil {
				t.Error("unable to complete solution", err)
			}

			if result != tc.expected {
				t.Errorf("got %s, wanted %s", result, tc.expected)
			}
		})
	}
}

