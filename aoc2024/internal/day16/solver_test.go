func TestPartOne(t *testing.T) {
	testCases := []struct{
		name     string
		input    string
		expected string
	}{
		{
			name: "PartOne finds the lowest score to complete a reindeer maze",
			input: exampleReindeerMaze,
			expected: "7036",
		},
		{
			name: "PartOne finds the lowest score to complete a second reindeer maze",
			input: exampleReindeerMaze2,
			expected: "11048",
		},
	}

	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			t.Parallel()

			solver := Solver{}

			result, err := solver.SolvePartOne(tc.input)
			if err != nil {
				t.Error("unable to complete solution", err)
			}

			if result != tc.expected {
				t.Errorf("got %s, wanted %s", result, tc.expected)
			}
		})
	}
}

